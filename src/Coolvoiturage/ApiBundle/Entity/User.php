    /**
     * @var integer
     *
     * @ORM\Column(name="bann", type="integer", nullable=true)
     */
    private $bann;

    /**
     * @var \Circle
     *
     * @ORM\ManyToOne(targetEntity="Circle")
     * @ORM\JoinColumns({
